package br.com.geekuniversity.secao18;

//Realizando c√°lculos com Math
public class Programa40 {

	public static void main(String[] args) {		
		System.out.println(Math.E); //Constante
		
		System.out.println(Math.PI); //PI
		
		System.out.println(Math.sin(45)); //Seno
		
		System.out.println(Math.cos(45));//Coseno
		
		System.out.println(Math.tan(45)); //Tangente
		
		System.out.println(Math.round(Math.PI)); //Arredondando para baixo
		
		System.out.println(Math.ceil(Math.PI)); //Arredondando para cima
		
		
		System.out.println(Math.floor(Math.PI)); //Arredondando para baixo e retorna um double/float
	
		System.out.println(Math.pow(3, 2)); //Elevando 3 ao quadrado
		
		System.out.println(Math.round(Math.pow(3, 2))); //Arredondando Elevando 3 ao quadrado

		System.out.println(Math.floor(Math.pow(3, 2))); //Arredondando Elevando 3 ao quadrado

		System.out.println(Math.sqrt(9));
		
		System.out.println(Math.round(Math.sqrt(9)));

	
	}

}
